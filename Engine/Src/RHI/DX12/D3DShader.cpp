	void* D3DShader::GetByteCode(EShaderType type)
	{
		switch (type)
		{
		case Ailu::EShaderType::kVertex:
			return reinterpret_cast<void*>(_p_vblob.Get());
		case Ailu::EShaderType::kPixel:
			return reinterpret_cast<void*>(_p_pblob.Get());
		}
		return nullptr;
	}

